			<p><font class="textbold">General Information</font></p>
			<p><font class="text"><i>The Bowdoin Orient</i> is a student 
				newspaper serving the entire Bowdoin community.  The paper has 
				an on-campus distribution of over 2000, and is mailed off-campus to 
				several hundred subscribers. The paper is distributed free-of-charge at 
				various locations on campus, including the student union, several classroom 
				buildings, the admissions office, the dining halls, and the libraries. Published 
				on Fridays, 24 times a year, the <i>Orient</i> is read by students and their families, 
				faculty, staff, alumni, off-campus subscribers, prospective students, and campus visitors.</font></p>
            <p><font class="textbold">History of the <i>Orient</i></font></p>



			<p><font class="text"><i>The Bowdoin Orient</i> was established in 1871 as Bowdoin 
				College's newspaper and literary magazine. Originally issued bi-weekly, it has been 
				a weekly since April, 1899. It is considered to be the oldest continuously-published 
				college weekly in the U.S., which means that it has been in publication every academic 
				year that Bowdoin has been in session since it began publishing weekly (other college 
				weeklies stopped printing during certain war years).</font></p>

            <p><font class="text">In the beginning, the <i>Orient</i> was laid out in a smaller 
			magazine format, and included literary material such as poems and 
			fiction alongside its news. In 1897, the literary society formed its own publication, 
			The Quill, and the <i>Orient</i> has since primarily focused on reporting news. In 1921, the <i>Orient</i> 
			moved from the magazine format to a larger broadsheet layout and has changed between 
			broadsheet and tabloid sizes frequently.</font></p>

            <p><font class="text">In 1912, The Bowdoin Publishing Company was established 
			as the formal publisher of the <i>Orient</i>, and remained independent of the College 
			for many years while using college facilities and working with faculty-member advisers. 
			The Bowdoin Publishing Company was a legal, non-profit corporation in the State of Maine 
			for many years (at least from 1968 to 1989), though it was most likely an independent 
			corporation since its inception.  In recent years, the <i>Orient</i> printed with the presses of 
			the Brunswick Times Record, though switched to using Central Maine Papers (publisher of <i>The 
			Kennebec Journal</i>) in 2005 and increased the number of full-color pages per issue from two to four.</font></p>

            <p><font class="text">The Orient building has its own archives, 
			with issues dating back to 1873, but it is missing several periods of time. 
			The Hawthorne-Longfellow Library has a nearly complete archive of past <i>Orient</i> 
			issues, both in print and on microform. Virtually all print issues are available 
			from 1871 to the present in Special Collections and Archives. Bound copies from 
			1871 to 1921 can be found in the periodicals section of the library. Microform 
			is available for issues 1921 to the present, and can be accessed at any time.</font></p> 

