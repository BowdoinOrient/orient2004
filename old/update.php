<p align="left"><font class="text">The Bowdoin community was saddened Thursday by news of the death of Sabrina Sampson-Knapp '08.</font></p>
<p align="left"><font class="text">Below is Dean of Student Affairs Craig Bradley's email to the College:</font></p>

<blockquote><font class="text"><i>December 9, 2004</i></font></blockquote>

<blockquote><font class="text"><i>Dear members of the Bowdoin community:</i></font></blockquote>

<Blockquote><font class="text"><i>It is with great sadness that I write to inform you of the death 
yesterday of Sabrina Sampson-Knapp '08.  Sabrina, who went on leave from 
Bowdoin in September, was at home in Philadelphia when she took her own 
life after suffering from a severe three-month depression.</i></font></blockquote>

<blockquote><font class="text"><i>While Sabrina was here only a short time, she made a number of 
meaningful connections on campus with students, faculty, coaches, and 
staff.  A resident of Maine Hall, she is described by those who knew her 
as a bright and powerful spirit.</i></font></blockquote>

<blockquote><font class="text"><i>I realize that this message may be shocking and upsetting to 
some of you, and, in addition to the support you will provide to one 
another,  I remind you that there are many members of our community who 
are here for you as well.  Members of the Counseling Service staff, the 
Student Affairs staff, and the Residential Life staff are available at 
any time of day or night by calling 725-3314.</i></font></blockquote>

<blockquote><font class="text"><i>A memorial service for Sabrina will be held on Saturday at 1 
p.m. at Friends Meeting House, 1515 Cherry Street, Philadelphia.  
Expressions of condolences may be sent to Sabrina's parents, Linda Knapp 
and Maurice Sampson, 129 West Gorgas Lane, Philadelphia, PA, 19119.</i></font></blockquote>

<blockquote><font class="text"><i>Please join me in keeping Sabrina's family in our thoughts and 
prayers during this painful time.</i></font></blockquote>

<blockquote><font class="text"><i>Sincerely,</i></font></blockquote>

<blockquote><font class="text"><i>Craig W. Bradley<br>
Dean of Student Affairs</i></font></blockquote>
 




